export const SCHEMA_MD = `---
description: Rules that govern this knowledge wiki. Read this before ingesting, querying, or linting.
wikiRole: schema
---

# Knowledge wiki schema

This stash follows Andrej Karpathy's LLM Wiki pattern, adapted for akm.

## Layout

\`\`\`
knowledge/
  schema.md      this file (the rulebook)
  index.md       catalog of pages by category (regenerable)
  log.md         append-only ingest/query/lint log
  raw/           immutable ingested sources (slug filenames)
  <page>.md      LLM-authored entity / concept / question / note pages
  <topic>/<page>.md   optional subfolders
\`\`\`

## Page frontmatter

Every page should carry frontmatter so akm can index and link it:

\`\`\`yaml
---
description: one-sentence summary used in search and lint
pageKind: entity | concept | question | note | <your-custom-kind>
xrefs:
  - knowledge:other-page
sources:
  - raw/2026-04-the-source-doc.md
---
\`\`\`

### Extending the taxonomy

\`pageKind\` accepts any non-empty string. The four built-ins above are just
defaults. To introduce a new category (e.g. \`decision-record\`, \`glossary\`):

- **One-off:** write \`pageKind: decision-record\` on a page and carry on.
  Subsequent ingests will see the new kind and keep using it.
- **First-class:** add it to \`knowledge.pageKinds\` in your akm config so the
  LLM is told about it during ingest and \`index.md\` gets a matching section.

The \`raw/\` folder uses \`wikiRole: raw\` and is immutable. \`schema.md\`,
\`index.md\`, \`log.md\` carry their own \`wikiRole\` and follow special rules.

## Three operations

### Ingest
1. New source goes into \`raw/<slug>.md\`. Never overwrite.
2. Find related pages with \`akm search "<key terms>" --type knowledge\`.
3. For each related page, decide: append a section, leave alone, or note a
   contradiction. Update xrefs both ways.
4. Create new pages for entities/concepts the source introduces.
5. Append one entry to \`log.md\` describing what was assimilated.

### Query
1. Search the wiki with \`akm search "<question>"\`.
2. Read the top hits with \`akm show knowledge:<page>\`.
3. If the answer is valuable enough to keep, file it back as a new page or
   amend an existing one (this is itself an ingest with the answer as raw).

### Lint
1. Run \`akm lint\` periodically.
2. Resolve contradictions, link orphans, refresh stale claims, add missing
   cross-references.

## Hard rules
- \`raw/\` is immutable. Never edit ingested sources.
- Cross-references must point at pages that actually exist.
- Prefer appending to an existing page over duplicating one.
- Cite the source page id (e.g. \`raw/2026-04-foo.md\`) when copying claims.
`;

import { pageKindHeading } from "../knowledge-page-kinds";

/**
 * Generate the scaffolded `index.md` with a section for each configured
 * page kind. This lets the index reflect the active taxonomy: the built-in
 * defaults (entity/concept/question/note) plus anything the user has added
 * via `knowledge.pageKinds` in config.
 */
export function buildIndexMd(pageKinds: readonly string[]): string {
  const sections = pageKinds.map((kind) => `## ${pageKindHeading(kind)}\n\n_(none yet)_\n`).join("\n");
  return `---
description: Catalog of pages in this knowledge wiki, by category. Regenerate as new pages are added.
wikiRole: index
---

# Index

_This file is regenerated by ingest and lint runs. Manual edits are fine but may be reorganized. Add a new \`## Heading\` section any time you introduce a new page kind._

${sections}`;
}

